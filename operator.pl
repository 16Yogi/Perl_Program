$a = 1;
$b = 2;

# arithmetic Operator
print("Arithmetic Operator\n");
print($a+$b," \n");
print($a-$b ,"\n");
print($a/$b ,"\n");
print($a*$b," \n");
print($a % $b ,"\n");
print($a ** $b ,"\n");
print("--------------\n");

print("Equality Operators\n");
print($a==$b,"\n");
print($a!=$b,"\n");
print($a<=>$b,"\n");
print($a>$b,"\n");
print($a<$b,"\n");
print($a>=$b,"\n");
print($a<=$b,"\n");
print("-------------------------\n");

print("assignment operator\n");
print($a==$b,"\n");
print($a+=$b,"\n");
print($a-=$b,"\n");
print($a/=$b,"\n");
print($a*=$b,"\n");
print($a%=$b,"\n");
print("------------------------\n");

print("logical Operator\n");
print(($a == 1) and ($b<=10),"\n");
print(($a == 1) && ($b<=10),"\n");
print(($>=1) or ($b<=10),"\n");
print(($>=1) || ($b<=10),"\n");
print(not ($b<=10),"\n");
print(!($b<=10),"\n");


